// TestHTTPErrorScenarios is a table-driven test for various error responses.
func TestHTTPErrorScenarios(t *testing.T) {
	ts := newTestStack(t)
	defer ts.cleanup()

	srv := httptest.NewServer(ts.server.Handler())
	defer srv.Close()

	cases := []struct {
		name       string
		method     string
		path       string
		body       string
		wantStatus int
	}{
		{
			name:       "POST invalid JSON",
			method:     http.MethodPost,
			path:       "/api/tasks",
			body:       `{invalid`,
			wantStatus: http.StatusBadRequest,
		},
		{
			name:       "POST empty body",
			method:     http.MethodPost,
			path:       "/api/tasks",
			body:       "",
			wantStatus: http.StatusBadRequest,
		},
		{
			name:       "GET nonexistent task",
			method:     http.MethodGet,
			path:       "/api/tasks/00000000-0000-0000-0000-000000000000",
			body:       "",
			wantStatus: http.StatusNotFound,
		},
		{
			name:       "DELETE nonexistent task",
			method:     http.MethodDelete,
			path:       "/api/tasks/00000000-0000-0000-0000-000000000000",
			body:       "",
			wantStatus: http.StatusNotFound,
		},
	}

	for _, tc := range cases {
		t.Run(tc.name, func(t *testing.T) {
			var bodyReader *bytes.Reader
			if tc.body != "" {
				bodyReader = bytes.NewReader([]byte(tc.body))
			}

			var req *http.Request
			var err error
			if bodyReader != nil {
				req, err = http.NewRequest(tc.method, srv.URL+tc.path, bodyReader)
			} else {
				req, err = http.NewRequest(tc.method, srv.URL+tc.path, nil)
			}
			if err != nil {
				t.Fatalf("build request: %v", err)
			}
			if tc.method == http.MethodPost {
				req.Header.Set("Content-Type", "application/json")
			}

			resp, err := http.DefaultClient.Do(req)
			if err != nil {
				t.Fatalf("%s %s: %v", tc.method, tc.path, err)
			}
			resp.Body.Close()

			if resp.StatusCode != tc.wantStatus {
				t.Errorf("expected %d, got %d", tc.wantStatus, resp.StatusCode)
			}
		})
	}
}

// TestMultipleTasksCompleteHTTP submits 3 tasks and verifies all complete with output files.
func TestMultipleTasksCompleteHTTP(t *testing.T) {
	ts := newTestStack(t)
	defer ts.cleanup()

	srv := httptest.NewServer(ts.server.Handler())
	defer srv.Close()

	targets := []string{"a.go", "b.go", "c.go"}
	taskIDs := make([]string, 0, len(targets))

	for _, target := range targets {
		body, _ := json.Marshal(map[string]interface{}{
			"projectPath":  ts.tmpDir,
			"targetFile":   target,
			"instruction":  "generate code for " + target,
			"contextFiles": []string{},
		})
		resp, err := http.Post(srv.URL+"/api/tasks", "application/json", bytes.NewReader(body))
		if err != nil {
			t.Fatalf("POST task %s: %v", target, err)
		}
		if resp.StatusCode != http.StatusCreated {
			resp.Body.Close()
			t.Fatalf("task %s: expected 201, got %d", target, resp.StatusCode)
		}
		var created struct {
			TaskID string `json:"task_id"`
		}
		if err := json.NewDecoder(resp.Body).Decode(&created); err != nil {
			resp.Body.Close()
			t.Fatalf("decode task %s: %v", target, err)
		}
		resp.Body.Close()
		taskIDs = append(taskIDs, created.TaskID)
	}

	// Poll all tasks until COMPLETED (timeout 20s).
	deadline := time.Now().Add(20 * time.Second)
	for _, id := range taskIDs {
		for time.Now().Before(deadline) {
			r, err := http.Get(srv.URL + "/api/tasks/" + id)
			if err != nil {
				time.Sleep(250 * time.Millisecond)
				continue
			}
			var task domain.Task
			if err := json.NewDecoder(r.Body).Decode(&task); err != nil {
				r.Body.Close()
				time.Sleep(250 * time.Millisecond)
				continue
			}
			r.Body.Close()
			if task.Status == domain.StatusCompleted {
				break
			}
			if task.Status == domain.StatusFailed {
				t.Fatalf("task %s failed: %s", id, task.Logs)
			}
			time.Sleep(250 * time.Millisecond)
		}
	}

	// Verify output files exist.
	for _, target := range targets {
		path := ts.tmpDir + "/" + target
		content, err := os.ReadFile(path)
		if err != nil {
			t.Errorf("output file %s not written: %v", target, err)
			continue
		}
		if !bytes.Contains(content, []byte("MockLLM")) {
			t.Errorf("file %s: expected 'MockLLM' in content, got: %s", target, content)
		}
	}

	// Verify all 3 tasks are individually retrievable and COMPLETED.
	for i, id := range taskIDs {
		r, err := http.Get(srv.URL + "/api/tasks/" + id)
		if err != nil {
			t.Errorf("GET task %d: %v", i, err)
			continue
		}
		var task domain.Task
		if err := json.NewDecoder(r.Body).Decode(&task); err != nil {
			r.Body.Close()
			t.Errorf("decode task %d: %v", i, err)
			continue
		}
		r.Body.Close()
		if task.Status != domain.StatusCompleted {
			t.Errorf("task %d (%s): expected COMPLETED, got %s", i, id, task.Status)
		}
	}
}
