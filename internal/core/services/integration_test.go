// TestSSEEventLifecycle submits a task and verifies the correct SSE events
// are received in order: task.queued → task.processing → task.completed.
func TestSSEEventLifecycle(t *testing.T) {
	ts := newTestStack(t)
	defer ts.cleanup()

	srv := httptest.NewServer(ts.server.Handler())
	defer srv.Close()

	type sseEvent struct {
		Type   string `json:"type"`
		TaskID string `json:"taskId"`
	}

	events := make(chan sseEvent, 32)
	sseCtx, sseCancel := context.WithCancel(context.Background())
	defer sseCancel()

	// Start SSE reader goroutine
	go func() {
		req, err := http.NewRequestWithContext(sseCtx, http.MethodGet, srv.URL+"/api/events", nil)
		if err != nil {
			return
		}
		resp, err := http.DefaultClient.Do(req)
		if err != nil {
			return
		}
		defer resp.Body.Close()

		scanner := bufio.NewScanner(resp.Body)
		for scanner.Scan() {
			line := scanner.Text()
			if !strings.HasPrefix(line, "data: ") {
				continue
			}
			payload := strings.TrimPrefix(line, "data: ")
			var ev sseEvent
			if err := json.Unmarshal([]byte(payload), &ev); err != nil {
				continue
			}
			select {
			case events <- ev:
			case <-sseCtx.Done():
				return
			}
		}
	}()

	// Wait for the initial "connected" event
	select {
	case ev := <-events:
		if ev.Type != "connected" {
			t.Fatalf("expected first SSE event type 'connected', got %q", ev.Type)
		}
	case <-time.After(2 * time.Second):
		t.Fatal("timed out waiting for SSE connected event")
	}

	// Submit a task via HTTP
	body := map[string]interface{}{
		"projectPath":  ts.tmpDir,
		"targetFile":   "sse_out.go",
		"instruction":  "generate a bar function",
		"contextFiles": []string{},
	}
	reqJSON, err := json.Marshal(body)
	if err != nil {
		t.Fatalf("marshal: %v", err)
	}
	resp, err := http.Post(srv.URL+"/api/tasks", "application/json", bytes.NewReader(reqJSON))
	if err != nil {
		t.Fatalf("POST /api/tasks: %v", err)
	}
	defer resp.Body.Close()
	if resp.StatusCode != http.StatusCreated {
		t.Fatalf("expected 201, got %d", resp.StatusCode)
	}
	var created struct {
		TaskID string `json:"task_id"`
	}
	if err := json.NewDecoder(resp.Body).Decode(&created); err != nil {
		t.Fatalf("decode create response: %v", err)
	}
	if created.TaskID == "" {
		t.Fatal("expected non-empty task_id")
	}

	// Collect events matching the submitted task for up to 15s
	wanted := []string{"task.queued", "task.processing", "task.completed"}
	var collected []string
	timeout := time.After(15 * time.Second)
collect:
	for {
		select {
		case ev := <-events:
			if ev.TaskID == created.TaskID {
				collected = append(collected, ev.Type)
			}
			if len(collected) >= len(wanted) {
				break collect
			}
		case <-timeout:
			break collect
		}
	}

	sseCancel()

	// Verify order: task.queued → task.processing → task.completed
	if len(collected) != len(wanted) {
		t.Fatalf("expected %d events %v, got %d: %v", len(wanted), wanted, len(collected), collected)
	}
	for i, w := range wanted {
		if collected[i] != w {
			t.Errorf("event[%d]: expected %q, got %q", i, w, collected[i])
		}
	}
}

