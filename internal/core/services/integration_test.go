// TestCancelTaskHTTP submits two tasks through the HTTP API with a blocking LLM,
// cancels the second (still queued) task, and verifies it is marked CANCELLED.
func TestCancelTaskHTTP(t *testing.T) {
	release := make(chan struct{})

	ts := newTestStackWithLLM(t, &blockingMockLLM{releaseCh: release})
	// close(release) must run before ts.cleanup() so the blocking worker goroutine
	// is unblocked and can finish its current task before Stop() waits for it and
	// the database is closed. Defers run LIFO, so register cleanup first (runs last)
	// and release second (runs first).
	defer ts.cleanup()
	defer close(release)

	srv := httptest.NewServer(ts.server.Handler())
	defer srv.Close()

	// Submit task1 — the worker picks it up and blocks in the LLM call.
	task1JSON, _ := json.Marshal(map[string]interface{}{
		"projectPath":  ts.tmpDir,
		"targetFile":   "block.go",
		"instruction":  "blocking task",
		"contextFiles": []string{},
	})
	resp1, err := http.Post(srv.URL+"/api/tasks", "application/json", bytes.NewReader(task1JSON))
	if err != nil {
		t.Fatalf("POST task1: %v", err)
	}
	resp1.Body.Close()
	if resp1.StatusCode != http.StatusCreated {
		t.Fatalf("task1: expected 201, got %d", resp1.StatusCode)
	}

	// Give the worker time to dequeue task1 and block on the LLM.
	time.Sleep(100 * time.Millisecond)

	// Submit task2 — it enters the queue while the worker is busy.
	task2JSON, _ := json.Marshal(map[string]interface{}{
		"projectPath":  ts.tmpDir,
		"targetFile":   "cancel_me.go",
		"instruction":  "task to cancel",
		"contextFiles": []string{},
	})
	resp2, err := http.Post(srv.URL+"/api/tasks", "application/json", bytes.NewReader(task2JSON))
	if err != nil {
		t.Fatalf("POST task2: %v", err)
	}
	var created struct {
		TaskID string `json:"task_id"`
	}
	if err := json.NewDecoder(resp2.Body).Decode(&created); err != nil {
		t.Fatalf("decode task2 response: %v", err)
	}
	resp2.Body.Close()

	// Cancel task2 via DELETE.
	req, err := http.NewRequest(http.MethodDelete, srv.URL+"/api/tasks/"+created.TaskID, nil)
	if err != nil {
		t.Fatalf("build DELETE request: %v", err)
	}
	cancelResp, err := http.DefaultClient.Do(req)
	if err != nil {
		t.Fatalf("DELETE /api/tasks/%s: %v", created.TaskID, err)
	}
	cancelResp.Body.Close()
	if cancelResp.StatusCode != http.StatusNoContent {
		t.Fatalf("cancel: expected 204, got %d", cancelResp.StatusCode)
	}

	// Verify the task status is CANCELLED.
	getResp, err := http.Get(srv.URL + "/api/tasks/" + created.TaskID)
	if err != nil {
		t.Fatalf("GET task: %v", err)
	}
	defer getResp.Body.Close()
	var task domain.Task
	if err := json.NewDecoder(getResp.Body).Decode(&task); err != nil {
		t.Fatalf("decode task: %v", err)
	}
	if task.Status != domain.StatusCancelled {
		t.Errorf("expected status CANCELLED, got %s", task.Status)
	}
}

