// TestFullPipelineHTTP submits a task through the HTTP API and polls until it completes.
func TestFullPipelineHTTP(t *testing.T) {
	ts := newTestStack(t)
	defer ts.cleanup()

	srv := httptest.NewServer(ts.server.Handler())
	defer srv.Close()

	body := map[string]interface{}{
		"projectPath":  ts.tmpDir,
		"targetFile":   "out.go",
		"instruction":  "generate a foo function",
		"contextFiles": []string{},
	}
	reqJSON, err := json.Marshal(body)
	if err != nil {
		t.Fatalf("marshal: %v", err)
	}

	resp, err := http.Post(srv.URL+"/api/tasks", "application/json", bytes.NewReader(reqJSON))
	if err != nil {
		t.Fatalf("POST /api/tasks: %v", err)
	}
	defer resp.Body.Close()
	if resp.StatusCode != http.StatusCreated {
		t.Fatalf("expected 201, got %d", resp.StatusCode)
	}

	var created struct {
		TaskID string `json:"task_id"`
	}
	if err := json.NewDecoder(resp.Body).Decode(&created); err != nil {
		t.Fatalf("decode create response: %v", err)
	}
	if created.TaskID == "" {
		t.Fatal("expected non-empty task_id")
	}

	// Poll until status == COMPLETED (timeout 15 s)
	deadline := time.Now().Add(15 * time.Second)
	var finalStatus domain.TaskStatus
	for time.Now().Before(deadline) {
		time.Sleep(250 * time.Millisecond)

		r, err := http.Get(srv.URL + "/api/tasks/" + created.TaskID)
		if err != nil {
			continue
		}
		var task domain.Task
		if err := json.NewDecoder(r.Body).Decode(&task); err != nil {
			r.Body.Close()
			continue
		}
		r.Body.Close()
		finalStatus = task.Status
		if finalStatus == domain.StatusCompleted || finalStatus == domain.StatusFailed {
			break
		}
	}

	if finalStatus != domain.StatusCompleted {
		t.Errorf("expected status COMPLETED, got %s", finalStatus)
	}

	// Verify output file was written
	outPath := ts.tmpDir + "/out.go"
	content, err := os.ReadFile(outPath)
	if err != nil {
		t.Fatalf("output file not written: %v", err)
	}
	if !bytes.Contains(content, []byte("MockLLM")) {
		t.Errorf("expected output to contain 'MockLLM', got: %s", content)
	}
}

